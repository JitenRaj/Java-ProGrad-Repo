public class GroupOfCutterProblem {

    public static void main(String[] args) {

        String[] groupOfCutter = new String[]{ "nailCutter", "cookieCutter",
                "cableCutter", "vegetableCutters", "microCutter"} ;

        for(String cutter : groupOfCutter)
            System.out.println( cutter );
    }
}
